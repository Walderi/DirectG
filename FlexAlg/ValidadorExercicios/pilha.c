void PilhaVazia(TipoPilha pilha)
{
	compPilha.elementos=0;
	int i;
	for(i=0; i<MAX_TAM_PILHA; i++)
	{
		compPilha.pilha[i].lineNo=0;
		compPilha.pilha[i].Errno = NULL; 	
	} 
}

